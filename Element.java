
public class Element {
	
	String data;
	Element nextElement;

	public String getData() {
		return data;
	}
	public void setData(String data) {
		this.data = data;
	}
	public Element getNextElement() {
		return nextElement;
	}
	public void setNextElement(Element nextElement) {
		this.nextElement = nextElement;
	}
}